import Foundation

class Calculadora {
    

func soma (numberOne:Double, numberTwo:Double) -> Double {
        
       return numberOne + numberTwo
        
    }
    func divisão (numberOne:Double, numberTwo:Double) -> Double {
        
       return numberOne / numberTwo
        
    }
    
    func multiplicação (numberOne:Double, numberTwo:Double) -> Double {
        
       return numberOne *  numberTwo
        
    }
    func subtração (numberOne:Double, numberTwo:Double) -> Double {
        
       return numberOne +  numberTwo
        
    }
    
}



